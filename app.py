# create a compressed model file which can be deployed on streamlit via github
# https://srivastavprojyot.medium.com/compress-large-ml-pickle-files-for-deployment-6f6afe315380

def compressed_pickle(title, data):

    with bz2.BZ2File(title + '.pbz2', 'w') as f:
        pickle.dump(data, f)

compressed_pickle('similarity-pkl-compressed', similarity_original)

# Load any compressed pickle file
def decompress_pickle(file):
 data = bz2.BZ2File(file, 'rb')
 data = cPickle.load(data)
 return data

